use Doctrine\Persistence\ManagerRegistry;

/**
 * @method Product|null find($id, $lockMode = null, $lockVersion = null)
 * @method Product|null findOneBy(array $criteria, array $orderBy = null)
 * @method Product[]    findAll()
 * @method Product[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class ProductRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Product::class);
    }

    /**
     * @return Product[] Returns an array of Product objects
     */
    public function findAllEx(string $filterCategory = "", int $filterPrice = 0)
    {
        $query =  $this->createQueryBuilder('p')
            ->orderBy('p.id', 'ASC')
            ->setMaxResults(5);

        if ($filterCategory != "") {
            $query->andWhere('p.category = :category')
                ->setParameter('category', $filterCategory);
        }

        if ($filterPrice > 0) {
            $subQuery = $this->_em->createQueryBuilder()
                ->select('identity(i.product)')
                ->from('App:ProductPrice', 'i')
                ->andWhere('i.original <= :price');

            $query->andWhere(
                $query->expr()->in(
                    'p.id',
                    $subQuery->getDQL()
                )
            )->setParameter('price', $filterPrice);
        }

        return $query->getQuery()
            ->getResult();
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Product $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Product $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

